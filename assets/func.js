/* Open */
function openMobiMenu() {
    document.getElementById("myNav").style.height = "100%";
    document.getElementById("myNav").style.width = "100%";
  }
  
  /* Close */
  function closeMobiMenu() {
    document.getElementById("myNav").style.height = "0";
    document.getElementById("myNav").style.width = "100%";

  }